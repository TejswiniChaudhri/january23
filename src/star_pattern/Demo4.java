package star_pattern;

public class Demo4
{

	public static void main(String[] args) 
	
	{
   int row=9, i, j, k;
	
	for( i=row; i>=1; i--)
	{
	for (j=row; j>i; j--)	
	{
		
	System.out.print(" ");	
		
	}
	for(k=1; k<=i;k++)
	{
		
	System.out.print(" *");	
		
	}
	System.out.println();
	
	}
	
	}
	}
	

			


	


