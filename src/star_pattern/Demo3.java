package star_pattern;

public class Demo3 
{

public static void main(String[] args) {

	
	int row=9, i, j, k;
    for (i=0; i<row; i++)
    {
	for(j=i; j<row; j++)
	{
	
	System.out.print(" ");	
	}
	for(k=0; k<(i+1); k++)
	{
	System.out.print("* ");	
		
	}
	
	System.out.println();
	
	}
	
    }	
    }	
	

